import Foundation
import UIKit

extension UIColor {
    static func orangeRail() -> UIColor {
        return UIColor(red: 251/255, green: 208/255, blue: 176/255, alpha: 1)
    }
    
    class func logoColor() -> UIColor {
        return UIColor.white
    }
}
